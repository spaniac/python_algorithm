def solution(n, computers):
    answer = 0
    visited = [0] * n
    while 0 in visited:
        dfs(computers, visited.index(0), visited)
        answer += 1
    return answer


def dfs(computers, start, visited):
    stack = [start]
    while stack:
        node = stack.pop()
        if visited[node] == 0:
            visited[node] = 1
        for i in range(len(computers)):
            if computers[node][i] == 1 and visited[i] == 0:
                stack.append(i)


